function evenOdd(number) {
    if (number % 2 == 0) {
        return true;
    }
    else {
        return false;
    }

}

var number = 120;
var myNumberIsEven = evenOdd(number);
console.log(myNumberIsEven);

var number = 121;
var myNumberIsEven = evenOdd(number);
console.log(myNumberIsEven);

